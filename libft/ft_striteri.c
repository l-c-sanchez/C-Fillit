#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int a;

	a = 0;
	if (!s || !f)
		return ;
	while (*s != '\0')
	{
		(*f)(a, s);
		a++;
		s++;
	}
}
