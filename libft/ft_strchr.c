#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *s1;

	s1 = (char *)s;
	if (c == '\0')
	{
		return ((char *)(s1 + ft_strlen(s1)));
	}
	while (*s1 != '\0')
	{
		if (*s1 == c)
		{
			return ((char *)s1);
		}
		s1++;
	}
	return (NULL);
}
